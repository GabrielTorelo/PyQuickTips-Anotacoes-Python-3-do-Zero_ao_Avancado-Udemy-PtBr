print(int('1')) # Converte do tipo 'string' para 'int'
print(type(int('1'))) # Imprime o tipo 'int'

print(float('1')) # Converte do tipo 'string' para 'float'
print(type(float('1'))) # Imprime o tipo 'float'

print(bool('False')) # Converte do tipo 'string' para 'bool'
print(type(bool('False'))) # Imprime o tipo 'bool'

print(str(1)) # Converte do tipo 'int' para 'string'
print(type(str(1))) # Imprime o tipo 'string'

print(str(1.5)) # Converte do tipo 'float' para 'string'
print(type(str(1.5))) # Imprime o tipo 'string'

print(str(True)) # Converte do tipo 'bool' para 'string'
print(type(str(True))) # Imprime o tipo 'string'
