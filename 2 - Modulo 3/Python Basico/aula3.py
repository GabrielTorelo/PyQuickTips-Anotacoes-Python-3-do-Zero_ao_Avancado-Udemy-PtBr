print(123) # Imprime 123
print(type(123)) # Imprime o tipo do dado (INTEGER - 'int')

print('Hello world!') # Imprime Hello world!
print(type('Hello world!')) # Imprime o tipo do dado (STRING - 'str')

print(10.2) # Imprime 10.2
print(type(10.2)) # Imprime o tipo do dado (FLOAT - 'float')

print(True) # Imprime True
print(type(True)) # Imprime o tipo do dado (BOOLEANO - 'bool')

print(30 > 31) # Imprime False
print(type(30 > 31)) # Imprime o tipo do dado (BOOLEANO - 'bool')
