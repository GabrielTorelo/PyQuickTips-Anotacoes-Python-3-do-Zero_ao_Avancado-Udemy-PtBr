# Maior
eh_maior = 10 > 5
print(f'Maior: {eh_maior}')

# Maior ou igual
eh_maior_igual = 10 >= 5
print(f'Maior ou igual: {eh_maior_igual}')

# Menor
eh_menor = 5 < 10
print(f'Menor: {eh_menor}')

# Menor ou igual
eh_menor_igual = 5 <= 10
print(f'Menor ou igual: {eh_menor_igual}')

# Igual
eh_igual = 5 == 5
print(f'Igual: {eh_igual}')

# Diferente
eh_diferente = 5 != 10
print(f'Diferente: {eh_diferente}')

# Pertence
lista = [1, 2, 3, 4, 5]
pertence = 3 in lista
print(f'Pertence: {pertence}')

# Não pertence
nao_pertence = 6 not in lista
print(f'Não pertence: {nao_pertence}')
